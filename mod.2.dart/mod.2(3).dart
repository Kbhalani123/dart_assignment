// Write a program to find the Max number from the given three number using Nested If.

void main()
{
  var n1=10, n2=20, n3=30;
  if(n1>n2 && n1>n3) {
    print('n1 is max number');
  }
  else if(n2>n1 && n2>n3){
    print('n1 is max number');
  }
  else{
    print('n3 is max number');
  }
}